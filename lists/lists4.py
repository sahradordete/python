colors = ["Blue", "Red", "Yellow", "Green"]
print(colors)

colors.sort() #it sorts the list alphabetically
print(colors)

colors.sort(reverse = True) #sorts the colors from Z - A
print(colors)

colors = ["Blue", "Red", "Yellow", "Green"]

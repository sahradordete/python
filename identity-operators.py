print("Logical Operators - IS")
a = 2
b = 6
c = a

print(a is b)
print(a is c)


print("Logical Operators - IS NOT")
a = 2
b = 6
c = a

print(a is not b)
print(a is not c)

print("------------------------------")

print("Membership Operators - IN")
# This operators evaluates to TRUE if the value is in the sequence
numbers = [1, 2, 3, 4]
print(2 in numbers)

colors = ["blue", "red", "yellow"]
print("green" in colors)

print("-------------------------------")
print("Membership Operators - NOT IN")
numbers = [1, 2, 3, 4]
print(2 not in numbers)


colors = ["blue", "red", "yellow"]
print("green" not in colors)

print("------------------------------")
print("Bitwise operators")
a = 24
b = 60
print(bin(a))
print(bin(b))


